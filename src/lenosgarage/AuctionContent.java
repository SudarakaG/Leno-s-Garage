public class AuctionContent {
    private String url;
    private String productName;
    private String rating;
    private String customer;
    private String date;
    private String reviewHead;
    private String reviewBody;

    /**
     * @return the url
     */
    public String getUrl() {
        return url;
    }

    /**
     * @param url the url to set
     */
    public void setUrl(String url) {
        this.url = url;
    }

    /**
     * @return the productName
     */
    public String getProductName() {
        return productName;
    }

    /**
     * @param productName the productName to set
     */
    public void setProductName(String productName) {
        this.productName = productName;
    }

    /**
     * @return the rating
     */
    public String getRating() {
        return rating;
    }

    /**
     * @param rating the rating to set
     */
    public void setRating(String rating) {
        this.rating = rating;
    }

    /**
     * @return the customer
     */
    public String getCustomer() {
        return customer;
    }

    /**
     * @param customer the customer to set
     */
    public void setCustomer(String customer) {
        this.customer = customer;
    }

    /**
     * @return the date
     */
    public String getDate() {
        return date;
    }

    /**
     * @param date the date to set
     */
    public void setDate(String date) {
        this.date = date;
    }

    /**
     * @return the reviewHead
     */
    public String getReviewHead() {
        return reviewHead;
    }

    /**
     * @param reviewHead the reviewHead to set
     */
    public void setReviewHead(String reviewHead) {
        this.reviewHead = reviewHead;
    }

    /**
     * @return the reviewBody
     */
    public String getReviewBody() {
        return reviewBody;
    }

    /**
     * @param reviewBody the reviewBody to set
     */
    public void setReviewBody(String reviewBody) {
        this.reviewBody = reviewBody;
    }
    
}
